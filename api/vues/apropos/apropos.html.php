                    <p>Pour Art public Montréal, le terme art public désigne l’ensemble des œuvres d’art situées dans des lieux d’accès public, extérieurs ou intérieurs. Les œuvres d’art public sont pérennes et installées dans des aires publiques communes. Elles se présentent de façon plus ou moins discrète, en affirmant leurs caractéristiques formelles, conceptuelles ou temporelles. Les oeuvres peuvent être liées à leur site ou non, s’adapter à leur environnement en étant en harmonie ou en contraste avec celui-ci, selon l’intention de l’artiste. </p>
                    <br>
                    <strong>Les lieux de l’art public</strong>
                    <br>
                    <p>On les retrouve dans des lieux transitoires ou de rencontre, telles les places publiques et les parcs, les stations de métro, les lieux culturels, les établissements d’enseignement, les édifices gouvernementaux, les sièges sociaux et succursales d’entreprises, les établissements du réseau de la santé, de même que les équipements sportifs et communautaires.</p>
                    <br>
                    <strong>Types d’œuvres</strong>
                    <br>
                    <p>Les œuvres sont issues de disciplines artistiques diverses (beaux-arts, arts décoratifs et design), font appel à divers matériaux et adoptent d’innombrables formes: sculptures, installations, peintures, photographies, verrières, mosaïques, œuvres technologiques, et bien plus.</p>
                    <br>
                    <strong>L’art mural</strong>
                    <br>
                    <p>Art public Montréal reconnaît le potentiel artistique et culturel des œuvres d’art mural qui enrichissent le paysage urbain et facilitent l’accès à l’art dans les quartiers montréalais. Ces murales, réalisées sur des murs et des infrastructures publiques ou privées, sont produites par des artistes professionnels, des muralistes ou collectifs d’artistes reconnus. Certaines sont considérées comme des destinations incontournables pour qui veut découvrir l’art public à Montréal.</p>
                    <br>
                    <p>En 2016, la Ville de Montréal se dote d’un Programme d’art mural visant à embellir l’espace public montréalais par la réalisation de murales extérieures visibles, créatives et liées à leur environnement. Le programme comprend trois volets répondant chacun à des objectifs et des critères d’appréciation distincts. L’un de ces volets est soutenu par l’Entente sur le développement culturel de Montréal.</p>
