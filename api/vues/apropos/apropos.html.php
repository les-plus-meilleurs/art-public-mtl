                    <p>La Politique d’intégration des arts à l’architecture et à l’environnement des bâtiments et des sites gouvernementaux et publics est une mesure qu’adopte le gouvernement du Québec dès 1961 et qui stipule qu’environ 1% du budget total de la construction d’un édifice ou d’un site public doit être consacré à l’intégration d’une œuvre d’art. Ce sont quelque 3500 œuvres issues de cette Politique qui forment aujourd’hui un riche patrimoine culturel dans toutes les régions du Québec.</p>
